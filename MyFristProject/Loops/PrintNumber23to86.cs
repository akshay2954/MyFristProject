using System;
using System.Collections.Generic;
using System.Text;

namespace MyFristProject.Loops
{
    class PrintNumber23to86
    {
        // Print Number between 23 to 86 using ( For loop)
        static void Main(string[] args)
        {
            Console.WriteLine("Answer:\n------");
            for (int i=23;i<=86;i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
