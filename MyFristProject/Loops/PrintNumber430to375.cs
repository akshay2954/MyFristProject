using System;
using System.Collections.Generic;
using System.Text;

namespace MyFristProject.Loops
{
    class PrintNumber430to375
    {
        //Print  Number Between 430 to 375 using( for loop_)
        static void Main(string[] args)
        {
            Console.WriteLine("Answer:\n-------");
            for(int i=430;i>=375;i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
